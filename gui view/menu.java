   public void loadCustomerData() {
    customerTableModel.setRowCount(0);
    
    // Query now selects ALL 6 fields from the Customers table
    String sql = "SELECT CustomerID, FirstName, LastName, Phone, Email, Address FROM Customers"; 
    
    try (Connection conn = DBConnection.getConnection();
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery(sql)) {

        while (rs.next()) {
            // Instantiate the 6-argument Customer object
            Customer customer = new Customer(
                rs.getInt("CustomerID"),
                rs.getString("FirstName"),
                rs.getString("LastName"),
                rs.getString("Phone"),
                rs.getString("Email"),
                rs.getString("Address") // <-- Read the new Address field
            );

            // Add the 6 fields to the table model row
            Object[] rowData = {
                customer.getCustomerID(),
                customer.getFirstName(),
                customer.getLastName(),
                customer.getPhone(),
                customer.getEmail(),
                customer.getAddress() // <-- Add to the row
            };
            customerTableModel.addRow(rowData);
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,
            "Customer DB Read Error: " + e.getMessage(),
            "SQL Error - Customers", JOptionPane.ERROR_MESSAGE);
    }
}
    
