    public void loadProductData() { 
    productTableModel.setRowCount(0); 
    
   
    String sql = "SELECT ProductID, ProductName, Manufacturer, `Condition`, AvailableQuantity, InventoryStatus FROM Products";
    
    try (Connection conn = DBConnection.getConnection(); 
         Statement stmt = conn.createStatement();         
         ResultSet rs = stmt.executeQuery(sql)) {       

        while (rs.next()) {
            Product product = new Product(
                rs.getInt("ProductID"),
                rs.getString("ProductName"),
                rs.getString("Manufacturer"),
                rs.getString("Condition"),
                rs.getInt("AvailableQuantity"),
                rs.getString("InventoryStatus")
            );

            
            Object[] rowData = {
                product.getProductID(),
                product.getProductName(),
                product.getManufacturer(),
                product.getCondition(),
                product.getAvailableQuantity(),
                product.getInventoryStatus()
            };
            productTableModel.addRow(rowData);
        }
        
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, 
            "Product DB Read Error: " + e.getMessage() + 
            "\nCheck your SQL query and DBConnection credentials.", 
            "SQL Error - Products", 
            JOptionPane.ERROR_MESSAGE);
    }
}
    
